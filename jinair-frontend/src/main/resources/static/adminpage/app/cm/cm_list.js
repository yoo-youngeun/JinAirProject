(function ($) {

    let indexBtn = [];

    let pagination = {
        total_page : 0,
        total_element : 0,
        current_page : 0,
        curren_elements : 0
    }

    // 페이지 정보
    let showPage = new Vue({
        el : '#showPage',
        data : {
            totalElements : {},
            currentPage : {}
        }
    })

    let memberList = new Vue({
        el : '#memList',
        data : {
            memberList : {}
        }
    })

    let page;

    list(0);

    function list(page){
        console.log("page : " + page);
        $.get("/api/user?page="+page, function (response){
            console.dir(response)
            indexBtn = [];
            pagination = response.pagination;

            showPage.totalElements = pagination.currentPage;
            showPage.currentPage = pagination.currentPage;
            console.dir(showPage);

            memberList.memberList = response.data;

            let lastPage = response.pagination.totalPages;
            let str = "";
            for (let i = 0; i < lastPage; i++) {
                str += "<td class='pageNum' id="+i+">" + (i+1) + "</td>";
            }
            $("#showPage").html(str);
            if(page == 0) {
                $(".firstPage1").css("visibility", "hidden");
            }
            if(page == lastPage-1) {
                $(".lastPage1").css("visibility", "hidden");
            }
            $(".pageNum").css({
                "background-color" : "#fff",
                "color" : "#444",
                "cursor" : "pointer"
            });
            $("#"+page+"").css({
                "background-color" : "#661e43",
                "color" : "white"
            });
            if (lastPage != 0) {
                str += "<td class='firstPage1'><<</td>";
            }
            if (lastPage != 0){
                str += "<td class='lastPage1'>>></td>";
            }
            $(document).on('click', '.firstPage2', function(){
                list(0);
            });
            $(document).on('click', '.lastPage2', function(){
                list(lastPage-1);
            });
        })
    }

    function search(id){
        $.get("/api/user/search/" + id, function (response){
            console.dir(response);
            memberList.memberList = response.data;

            $("#footer").css("display", "none");
        })
    }

    $("#searchSubmit").click(() => {
        if(!$("#searchUserid").val()){
            alert('입력을 확인해주세요')
        }else{
            search($("#searchUserid").val());
        }
    })

    $(document).on('click', '.pages', function(){
        let pageId = this.id;
        list(pageId);
    });



})(jQuery);
